public class Circle {

	public static void main(String[] args) {

		int radius = 5;
		double area = radius * radius * Math.PI;
		double diameter = radius * 2;


		System.out.printf("The circle's radius is %d, the area is %f. \n"
				+ " The diameter is %f ", radius, area, diameter);
		
}
}
